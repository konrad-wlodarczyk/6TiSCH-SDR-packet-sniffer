        self.blocks_ctrlport_monitor_0 = not True or monitor()
        self.analog_quadrature_demod_cf_1 = analog.quadrature_demod_cf(25)
        self.analog_pwr_squelch_xx_0 = analog.pwr_squelch_cc((-30), 1, 0, True)


